'''
 Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

num1 = float(input("Me informe um número: "))
num2 = float(input("Me informe outro número: "))
num3 = float(input("Me informe um último número: "))

print("A média aritmética desses três números é ", (num1 + num2 + num3) / 3)